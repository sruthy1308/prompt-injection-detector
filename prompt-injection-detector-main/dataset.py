# Build dataset
texts = safe + suspicious + malicious
labels = [0]*len(safe) + [1]*len(suspicious) + [2]*len(malicious)

print(f"Dataset: {len(safe)} safe, {len(suspicious)} suspicious, {len(malicious)} malicious = {len(texts)} total")

# Split
X_train, X_test, y_train, y_test = train_test_split(
    texts, labels, test_size=0.2, random_state=42, stratify=labels
)

# TF-IDF with character + word ngrams
vectorizer = TfidfVectorizer(
    ngram_range=(1, 3),
    max_features=20000,
    sublinear_tf=True,
    analyzer='word',
    min_df=1,
)
X_train_vec = vectorizer.fit_transform(X_train)
X_test_vec = vectorizer.transform(X_test)

# Ensemble: CalibratedLinearSVC + LogisticRegression
svc = CalibratedClassifierCV(LinearSVC(C=1.0, max_iter=2000), cv=3)
lr = LogisticRegression(C=2.0, max_iter=1000, solver='lbfgs')

svc.fit(X_train_vec, y_train)
lr.fit(X_train_vec, y_train)

# Soft voting ensemble
svc_proba = svc.predict_proba(X_test_vec)
lr_proba = lr.predict_proba(X_test_vec)
ensemble_proba = (svc_proba + lr_proba) / 2
y_pred = np.argmax(ensemble_proba, axis=1)

acc = accuracy_score(y_test, y_pred)
print(f"\nTest Accuracy: {acc*100:.2f}%")
print("\nClassification Report:")
print(classification_report(y_test, y_pred, target_names=["Safe", "Suspicious", "Malicious"]))

# Cross-validation on full dataset
X_all_vec = vectorizer.transform(texts)
cv_scores = cross_val_score(lr, X_all_vec, labels, cv=5, scoring='accuracy')
print(f"5-Fold CV Accuracy: {cv_scores.mean()*100:.2f}% (+/- {cv_scores.std()*100:.2f}%)")

# Save vectorizer
with open("vectorizer.pkl", "wb") as f:
    pickle.dump(vectorizer, f)

from model import EnsembleModel

# Retrain on full data for final model
X_all_vec_fit = vectorizer.transform(texts)
svc_final = CalibratedClassifierCV(LinearSVC(C=1.0, max_iter=2000), cv=3)
lr_final = LogisticRegression(C=2.0, max_iter=1000, solver='lbfgs')
svc_final.fit(X_all_vec_fit, labels)
lr_final.fit(X_all_vec_fit, labels)

model = EnsembleModel(svc_final, lr_final)

with open("model.pkl", "wb") as f:
    pickle.dump(model, f)

